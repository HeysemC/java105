package ders12_forLoops;

public class C01_ForLoops {
    public static void main(String[] args) {
        String str="Java\n";
        System.out.println(str.repeat(10));
        //repeat olmasaydi
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        for (int i =0; i<10; i++){
            //sart saglandiginda calisacak kod
            System.out.println(i + " ");
        }

    }
}
